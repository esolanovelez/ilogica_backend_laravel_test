                        <div class="col-md-12 py-3 py-md-2">
                            <p class="fg-primary fw-medium fs-vlarge">The Data Protection Act</p>
                            <p class="mb-0 text-justify">
                                The Data Protection Act 2018 controls how your personal information is 
                                used by organisations, businesses or the government.
                                <br><br>
                                The Data Protection Act 2018 is the UK’s implementation of the General 
                                Data Protection Regulation (GDPR).
                                <br><br>
                                Everyone responsible for using personal data has to follow strict rules called 
                                ‘data protection principles’. They must make sure the information is:
                            </p>
                            <br>
                            <ul class="list-group">
                                <li class="list-group-item">used fairly, lawfully and transparently</li>
                                <li class="list-group-item">used for specified, explicit purposes</li>
                                <li class="list-group-item">used in a way that is adequate, relevant and limited to only what is necessary</li>
                                <li class="list-group-item">accurate and, where necessary, kept up to date</li>
                                <li class="list-group-item">kept for no longer than is necessary</li>
                                <li class="list-group-item">handled in a way that ensures appropriate security, including protection against unlawful or unauthorised processing, access, loss, destruction or damage</li>
                            </ul>
                            <br>
                            <p class="mb-0 text-justify">There is stronger legal protection for more sensitive information, such as:</p>
                            <br>
                            <ul class="list-group">
                                <li class="list-group-item">race</li>
                                <li class="list-group-item">ethnic background</li>
                                <li class="list-group-item">political opinions</li>
                                <li class="list-group-item">religious beliefs</li>
                                <li class="list-group-item">trade union membership</li>
                                <li class="list-group-item">genetics</li>
                                <li class="list-group-item">biometrics (where used for identification)</li>
                                <li class="list-group-item">health</li>
                                <li class="list-group-item">sex life or orientation</li>
                            </ul>
                            <br>
                            <p class="mb-0 text-justify">There are separate safeguards for personal data relating to criminal convictions and offences.</p>
                            <br>
                            <p class="fg-primary fw-medium fs-vlarge">Your rights</p>
                            <br>
                            <p class="mb-0 text-justify">Under the Data Protection Act 2018, you have the right to find out what information the government and other organisations store about you. These include the right to:</p>
                            <ul class="list-group">
                                <li class="list-group-item">be informed about how your data is being used</li>
                                <li class="list-group-item">access personal data</li>
                                <li class="list-group-item">have incorrect data updated</li>
                                <li class="list-group-item">have data erased</li>
                                <li class="list-group-item">stop or restrict the processing of your data</li>
                                <li class="list-group-item">data portability (allowing you to get and reuse your data for different services)</li>
                                <li class="list-group-item">object to how your data is processed in certain circumstances</li>
                            </ul>
                            <br>
                            <p class="mb-0 text-justify">You also have rights when an organisation is using your personal data for:</p>
                            <br>
                            <ul class="list-group">
                                <li class="list-group-item">automated decision-making processes (without human involvement)</li>
                                <li class="list-group-item">profiling, for example to predict your behaviour or interests</li>
                            </ul>
